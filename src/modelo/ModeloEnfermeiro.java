
package modelo;


public class ModeloEnfermeiro {

    
    private int code;
    private String nomee;
    private String ende;
    private String tele;
    
    private String pesquisa;
    
    
    /**
     * @return the code
     */
    public int getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(int code) {
        this.code = code;
    }

    /**
     * @return the nomee
     */
    public String getNomee() {
        return nomee;
    }

    /**
     * @param nomee the nomee to set
     */
    public void setNomee(String nomee) {
        this.nomee = nomee;
    }

    /**
     * @return the ende
     */
    public String getEnde() {
        return ende;
    }

    /**
     * @param ende the ende to set
     */
    public void setEnde(String ende) {
        this.ende = ende;
    }

    /**
     * @return the tele
     */
    public String getTele() {
        return tele;
    }

    /**
     * @param tele the tele to set
     */
    public void setTele(String tele) {
        this.tele = tele;
    }

    /**
     * @return the pesquisa
     */
    public String getPesquisa() {
        return pesquisa;
    }

    /**
     * @param pesquisa the pesquisa to set
     */
    public void setPesquisa(String pesquisa) {
        this.pesquisa = pesquisa;
    }
    
}
