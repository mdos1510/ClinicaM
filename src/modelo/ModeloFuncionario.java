
package modelo;


public class ModeloFuncionario {

   private int codf;
   private String nomef;
   private String endf;
   private String telf;
   private String cargof;
   
   private String pesquisa;
    
    
    /**
     * @return the codf
     */
    public int getCodf() {
        return codf;
    }

    /**
     * @param codf the codf to set
     */
    public void setCodf(int codf) {
        this.codf = codf;
    }

    /**
     * @return the nomef
     */
    public String getNomef() {
        return nomef;
    }

    /**
     * @param nomef the nomef to set
     */
    public void setNomef(String nomef) {
        this.nomef = nomef;
    }

    /**
     * @return the endf
     */
    public String getEndf() {
        return endf;
    }

    /**
     * @param endf the endf to set
     */
    public void setEndf(String endf) {
        this.endf = endf;
    }

    /**
     * @return the telf
     */
    public String getTelf() {
        return telf;
    }

    /**
     * @param telf the telf to set
     */
    public void setTelf(String telf) {
        this.telf = telf;
    }

    /**
     * @return the cargof
     */
    public String getCargof() {
        return cargof;
    }

    /**
     * @param cargof the cargof to set
     */
    public void setCargof(String cargof) {
        this.cargof = cargof;
    }

    /**
     * @return the pesquisa
     */
    public String getPesquisa() {
        return pesquisa;
    }

    /**
     * @param pesquisa the pesquisa to set
     */
    public void setPesquisa(String pesquisa) {
        this.pesquisa = pesquisa;
    }
   
}
